# Los numeros son objetos como todo en Ruby

# Division de enteros
puts "10 / 4 -->  #{10 / 4}"

# Division de flotantes
puts "10.0 / 4.0 -->  #{10.0 / 4.0}"

# Convertir entero a float 
puts "10.to_f -->  #{10.to_f}"

# Convertir float a entero 
puts "10.47.to_i -->  #{10.47.to_i}"

# Obtener valor absoluto 
puts "-10.abs -->  #{-10.abs}"

# Verificar si es par, retorna true o false
puts "10.even?  -->  #{10.even? }"
puts "11.even?  -->  #{11.even? }"

# Obtienen el siguente ordinal
puts "10.next -->  #{10.next}"